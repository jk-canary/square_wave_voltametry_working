void golay(int window_size, float buffer_size, float *poriginal_current, float *pgolay_current)
{
		for(int i=0; i< buffer_size; i++)
		{
			pgolay_current[i] = 0.0;
		}
		for(int i=0; i< buffer_size; i++)
		{
			int l=0;
			if(i<=(window_size-1))
			{
					for(int column=0;column<i+1;column++)
					{
						pgolay_current[i] += (pcoeff_[i][column] * poriginal_current[column]);
					}
			}
			else if(i >= window_size && i < (int)(buffer_size))
			{
				for(int j=window_size; j>0; j--)
				{
					pgolay_current[i] += (pcoeff_[window_size-1][l] * poriginal_current[i-j]);
					l++;
				}
			}
		}
}



void moving_window(int window_size, int iteration, float buffer_size,float *pgolay_current, float *pAverage)
{
		for(int i=0;i<buffer_size;i++)
		{
			golay_dummy[i] = pgolay_current[i];
		}
		for(int x=0; x< iteration; x++)
		{
			for(int i=0; i< buffer_size; i++)
			{
				float sum_val = 0;
				
				if(i<window_size)
				{
					for(int j=0; j<i+1; j++)
					{
						sum_val += golay_dummy[i-j];
					}
					pAverage[i] = sum_val/(i+1);
				}
				else if(i<= (int)buffer_size)
				{
					for(int j = window_size; j>0; j--)
					{
						sum_val += golay_dummy[i-j];
					}
					pAverage[i] = sum_val/window_size;
				}
			}
		for(int i=0;i<buffer_size;i++)
			{
				golay_dummy[i] = pAverage[i];
			}
		}
}
void fit_cubic_fun(int len, float *px_val, float *py_val, float *pfit_line, float *psub_val) {
    float sum_x = 0, sum_y = 0, sum_x2 = 0, sum_x3 = 0, sum_x4 = 0;
    float sum_x5 = 0, sum_x6 = 0, sum_xy = 0, sum_x2y = 0, sum_x3y = 0;
    float a = 0, b = 0, c = 0, d = 0;

    // Temporary substitute array
    float py_substitute[len];
    for (int i = 0; i < len; i++) 
		{
        py_substitute[i] = py_val[i];
    }

    // Iterative fitting process (3 iterations)
    for (int ct = 0; ct < 40; ct++) 
		{
        // Reset sums for each iteration
        sum_x = sum_y = sum_x2 = sum_x3 = sum_x4 = sum_x5 = sum_x6 = 0;
        sum_xy = sum_x2y = sum_x3y = 0;

        // Calculate the sums
        for (int i = 0; i < len; i++) 
				{
            float x = px_val[i];
            float y = py_substitute[i];
            sum_x += x;
            sum_y += y;
            sum_x2 += x * x;
            sum_x3 += x * x * x;
            sum_x4 += x * x * x * x;
            sum_x5 += x * x * x * x * x;
            sum_x6 += x * x * x * x * x * x;
            sum_xy += x * y;
            sum_x2y += x * x * y;
            sum_x3y += x * x * x * y;
        }

        // Coefficients for the normal equations
        float A[4][5] = {
            {sum_x6, sum_x5, sum_x4, sum_x3, sum_x3y},
            {sum_x5, sum_x4, sum_x3, sum_x2, sum_x2y},
            {sum_x4, sum_x3, sum_x2, sum_x, sum_xy},
            {sum_x3, sum_x2, sum_x, len, sum_y}
        };

        // Gaussian elimination to solve for a, b, c, d
        for (int i = 0; i < 4; i++) 
				{
            float diag = A[i][i];
            for (int j = 0; j < 5; j++) {
                A[i][j] /= diag;
            }
            for (int k = 0; k < 4; k++) {
                if (k != i) {
                    float factor = A[k][i];
                    for (int j = 0; j < 5; j++) {
                        A[k][j] -= factor * A[i][j];
                    }
                }
            }
        }

        // Extract coefficients
        a = A[0][4];
        b = A[1][4];
        c = A[2][4];
        d = A[3][4];

        // Update substitute array based on the cubic fit
        for (int i = 0; i < len; i++) {
            pfit_line[i] = a * px_val[i] * px_val[i] * px_val[i] + b * px_val[i] * px_val[i] + c * px_val[i] + d;
            if (pfit_line[i] > py_substitute[i]) {
                py_substitute[i] = py_substitute[i];
            } else {
                py_substitute[i] = pfit_line[i];
            }
        }
    }

    // Calculate the difference (residual) between original and fit line
    for (int i = 0; i < len; i++) {
        if ((py_val[i] - pfit_line[i]) >= 0) {
            psub_val[i] = py_val[i] - pfit_line[i];
        } else {
            psub_val[i] = 0;
        }
    }
}



